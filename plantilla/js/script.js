let operaciones = function(num1, num2) {
    console.log(num1 + " + " + num2 + " = " + (num1 + num2));
    console.log(num1 + " - " + num2 + " = " + (num1 - num2));
    console.log(num1 + " * " + num2 + " = " + (num1 * num2));
    console.log(num1 + " / " + num2 + " = " + (num1 / num2));
    console.log(num1 + " % " + num2 + " = " + (num1 % num2));
    console.log(num1 + " ** " + num2 + " = " + (num1 ** num2));
}

operaciones(5, 4);